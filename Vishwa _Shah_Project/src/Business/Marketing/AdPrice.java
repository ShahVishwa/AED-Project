package Business.Marketing;

/**
 *
 * @author Vishwa
 */
public class AdPrice {
    
    private float agePrice;
    private float pageHitRatePrice;
    private float consumerInterestPrice;
    private float adSpaceDimensionPrice;
    private float productPrice;

    public float getAgePrice() {
        return agePrice;
    }

    public void setAgePrice(float agePrice) {
        this.agePrice = agePrice;
    }

    public float getPageHitRatePrice() {
        return pageHitRatePrice;
    }

    public void setPageHitRatePrice(float pageHitRatePrice) {
        this.pageHitRatePrice = pageHitRatePrice;
    }

    public float getConsumerInterestPrice() {
        return consumerInterestPrice;
    }

    public void setConsumerInterestPrice(float consumerInterestPrice) {
        this.consumerInterestPrice = consumerInterestPrice;
    }

    public float getAdSpaceDimensionPrice() {
        return adSpaceDimensionPrice;
    }

    public void setAdSpaceDimensionPrice(float adSpaceDimensionPrice) {
        this.adSpaceDimensionPrice = adSpaceDimensionPrice;
    }

    public float getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(float productPrice) {
        this.productPrice = productPrice;
    }
    
    
    
}
