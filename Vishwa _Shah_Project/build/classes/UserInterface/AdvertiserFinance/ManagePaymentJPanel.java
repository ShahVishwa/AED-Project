    private void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)paymentJTable.getModel();
        dtm.setRowCount(0);
        for(Payment payment : advertiserFinance.getPaymentHistory().getPaymentList())
        {
            Object[] row = new Object[5];
            row[0] = payment.getInvoice().getPublisherName();
//            row[1] = payment.getInvoice().getSecs();
//            row[2] = payment.getInvoice().getRatePerSec();
//            row[3] = payment.getInvoice().getAmount();
            row[4] = payment.getInvoice().getCurrentDue();
            dtm.addRow(row);
        }
    }

