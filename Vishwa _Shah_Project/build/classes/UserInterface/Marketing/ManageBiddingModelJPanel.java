    private void enterJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterJButtonActionPerformed
        try{
        int minAge = Integer.parseInt(minAgeJTextField.getText());
        int maxAge = Integer.parseInt(maxAgeJTextField.getText());
        float pageHitRate = Float.parseFloat(pageHitRateJTextField.getText());
        String interest = interestJTextField.getText();
        float minHeight = Float.parseFloat(heightJTextField.getText());
        float minBreadth = Float.parseFloat(breadthJTextField.getText());
        if(interest.isEmpty() || interest.equals(null) || interest.trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter a valid String");
            return;
        }
        
        marketer.getAdPriceRules().setMinAge(minAge);
        marketer.getAdPriceRules().setMaxAge(maxAge);
        marketer.getAdPriceRules().setMinPageHitRate(pageHitRate);
        marketer.getAdPriceRules().setInterest(interest);
        marketer.getAdPriceRules().setMinAdSpaceHeight(minHeight);
        marketer.getAdPriceRules().setMinAdSpaceBreadth(minBreadth);
        
        float AgePrice = Float.parseFloat(priceForAgeJTextField.getText());
        float pageHitRatePrice = Float.parseFloat(priceForPageHitRateJTextField.getText());
        float interestPrice = Float.parseFloat(priceForInterestJTextField.getText());
        float adSpaceDimensionsPrice = Float.parseFloat(priceFordimensionsJTextField.getText());
        float productPrice = Float.parseFloat(priceForProductJTextField.getText());
        
        marketer.getAdPriceRules().getAdPrice().setAgePrice(AgePrice);
        marketer.getAdPriceRules().getAdPrice().setPageHitRatePrice(pageHitRatePrice);
        marketer.getAdPriceRules().getAdPrice().setConsumerInterestPrice(interestPrice);
        marketer.getAdPriceRules().getAdPrice().setAdSpaceDimensionPrice(adSpaceDimensionsPrice);
        marketer.getAdPriceRules().getAdPrice().setProductPrice(productPrice);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Enter in a valid Format");
            return;
        }
    }//GEN-LAST:event_enterJButtonActionPerformed


