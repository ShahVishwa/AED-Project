    private void populateFields()
    {
        for(WorkRequest request : account.getWorkQueue().getWorkRequestList())
        {
            messageJTextField.setText(request.getMessage());
            receiverJTextField.setText(String.valueOf(request.getReceiver()));
            statusJTextField.setText(request.getStatus());
            String result = ((PaymentRequest)request).getTestResult();
            if(result == null)
            {
                resultJTextField.setText("Waiting");
            }
            else
            {
            resultJTextField.setText(result);
            } 
        }
    }

