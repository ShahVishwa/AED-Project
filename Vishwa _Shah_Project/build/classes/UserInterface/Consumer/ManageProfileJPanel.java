    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        try{
        String name = nameJTextField.getText();
        int age = Integer.parseInt(ageJTextField.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");
        Date dob = null;
        dob = sdf.parse(birthdateChooserCombo.getText());
        java.sql.Date sqlDate = new java.sql.Date(dob.getTime());
                
        String location = locaionJTextField.getText();
        String interest = interestJTextField.getText();
        if(name.isEmpty() || name.equals(null) || name.trim().isEmpty() && location.isEmpty() || location.equals(null) || location.trim().isEmpty() && interest.isEmpty() || interest.equals(null) || interest.trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter a valid String");
            return;
        }
       
        else{
        consumerOrganization.getEmployeeDirectory().getEmployeelist().add(consumer);
        consumer.setName(name);
        consumer.setAge(age);
        consumer.setDOB(sqlDate);
        consumer.setLocation(location);
        consumer.setInterests(interest);
        }}
        catch(ParseException e)
        {
            e.printStackTrace();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Enter a Valid String");
            return;
        }
        
    }//GEN-LAST:event_createJButtonActionPerformed


