    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        try{
        Enterprise enterprise = (Enterprise)enterpriseJComboBox.getSelectedItem();
        String username = usernameJTextField.getText();
        String password = passwordJTextField.getText();
        String name = nameJTextField.getText();//Enterprise admin name
        
        if(username.isEmpty() || password.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter a valid String");
            return;
        }
        
        else if(username.contains("") || password.contains(""))
        {
            JOptionPane.showMessageDialog(null, "Space is not allowed");
            return;
        }
        else if(username.isEmpty() || username.equals(null) || username.trim().isEmpty() && password.isEmpty() || password.equals(null) || password.trim().isEmpty() && name.isEmpty() || name.equals(null) || name.trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter a valid String");
            return;
        }
        
        else if(enterprise instanceof PublisherEnterprise)
        {
            PublisherAdmin pa = new PublisherAdmin();
            pa.setName(name);
            
            UserAccount ua = new UserAccount();
            ua.setUsername(username);
            ua.setPassword(password);
            ua.setEmployee(pa);
            ua.setRole(new PublisherEnterpriseAdminRole());
            
            enterprise.getEmployeeDirectory().getEmployeelist().add(pa);
            enterprise.getUserAccountDirectory().getUserAccountList().add(ua);
            populateTable();
        }
        else if(enterprise instanceof AdvertiserEnterprise)
        {
            AdvertiserAdmin adadmin = new AdvertiserAdmin();
            adadmin.setName(name);
            
            UserAccount ua = new UserAccount();
            ua.setUsername(username);
            ua.setPassword(password);
            ua.setEmployee(adadmin);
            ua.setRole(new AdvertiserEnterpriseAdminRole());
            
            enterprise.getEmployeeDirectory().getEmployeelist().add(adadmin);
            enterprise.getUserAccountDirectory().getUserAccountList().add(ua);
            populateTable();
        }
        else if(enterprise instanceof AdExchangeEnterprise)
        {
            AdExchangeAdmin adexadmin = new AdExchangeAdmin();
            adexadmin.setName(name);
            
            UserAccount ua = new UserAccount();
            ua.setUsername(username);
            ua.setPassword(password);
            ua.setEmployee(adexadmin);
            ua.setRole(new AdExchangeEnterpriseAdminRole());
            
            enterprise.getEmployeeDirectory().getEmployeelist().add(adexadmin);
            enterprise.getUserAccountDirectory().getUserAccountList().add(ua);
            populateTable();
        }
        else if(enterprise instanceof ConsumerEnterprise)
        {
            ConsumerAdmin cadmin = new ConsumerAdmin();
            cadmin.setName(name);
            
            UserAccount ua = new UserAccount();
            ua.setUsername(username);
            ua.setPassword(password);
            ua.setEmployee(cadmin);
            ua.setRole(new ConsumerEnterpriseAdminRole());
            
            enterprise.getEmployeeDirectory().getEmployeelist().add(cadmin);
            enterprise.getUserAccountDirectory().getUserAccountList().add(ua);
            populateTable();
        }}
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Enter in a Valid Format");
            return;
        }


    }//GEN-LAST:event_submitJButtonActionPerformed

