    private void saveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJButtonActionPerformed
        try{
        String name = nameJTextField.getText();
        float height = Float.parseFloat(heightJTextField.getText());
        float breadth = Float.parseFloat(breadthJTextField.getText());
        if(name.isEmpty() || name.equals(null) || name.trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter a valid String");
            return;
        }
        else{
        page.setName(name);
        page.getAdSpace().setHeight(height);
        page.getAdSpace().setBreadth(breadth);
        populateFields();
        }}
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Enter in a valid Format");
            return;
        }
    }//GEN-LAST:event_saveJButtonActionPerformed

